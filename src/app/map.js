  const getTile = (layer, col, row) => {
    return layers[layer][row * cols + col]
  }

  const render = (layer, viewport) => {
    let minRow = Math.floor(viewport.top / tileSize)
    let maxRow = Math.floor(viewport.bottom / tileSize)
    let minCol = Math.floor(viewport.left / tileSize)
    let maxCol = Math.floor(viewport.right / tileSize)

    for (let i = minRow; i <= maxRow; ++i) {
      for (let j = minCol; j <= maxCol; ++j) {
        drawTile(layer, i * cols + j)
      }
    }
  }

  const drawTile = (layer, index) => {
    let row = index % rows
    let col = Math.floor(index / cols)
    switch (layers[layer][index]) {
      case 1:
        graphics.drawRect(tileSize * row, tileSize * col, tileSize, tileSize)
        break
      case 2:
        graphics.drawCircle(tileSize * row, tileSize * col, tileSize / 2)
    }
  }

  return {
    cols: cols,
    rows: rows,
    tileSize: tileSize,
    layers: layers,
    getTile: getTile,
    render: render
  }
